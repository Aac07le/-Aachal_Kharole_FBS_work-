
# WAP to calculate area of triangle and rectangle

# Area of triangle
base = int(input("Enter the base of triangle: "))
hight= int(input("Enter the hight of triangle: "))

area_of_triangle = 1/2 * base * hight 
print("The area of triangle: ", area_of_triangle)

#  Area of rectangle
length = int(input("Enter the length of rectangle:" ))
width = int(input("Enter width of rectangle: "))

area_of_rectangle = length * width
print("TThe area of rectangle:", area_of_rectangle)
 